function f = newU0(w, azx, t)
s2=1/(4-4^(1/3));
f=expm(-1i*s2/2*t*w*[1,0;0,-1]/2)...
    *expm(-1i*s2*t*((azx(1)+w)*[1,0;0,-1]+azx(2)*[0,1;1,0])/2)...
    *expm(-1i*s2*t*w*[1,0;0,-1]/2)...
    *expm(-1i*s2*t*((azx(1)+w)*[1,0;0,-1]+azx(2)*[0,1;1,0])/2)...
    *expm(-1i*(1-3*s2)/2*t*w*[1,0;0,-1]/2)...
    *expm(-1i*(1-4*s2)*t*((azx(1)+w)*[1,0;0,-1]+azx(2)*[0,1;1,0])/2)...
    *expm(-1i*(1-3*s2)/2*t*w*[1,0;0,-1]/2)...
    *expm(-1i*s2*t*((azx(1)+w)*[1,0;0,-1]+azx(2)*[0,1;1,0])/2)...
    *expm(-1i*s2*t*w*[1,0;0,-1]/2)...
    *expm(-1i*s2*t*((azx(1)+w)*[1,0;0,-1]+azx(2)*[0,1;1,0])/2)...
    *expm(-1i*s2/2*t*w*[1,0;0,-1]/2);
end